		Sigma SAT is a nonprofit organization that is dedicated to providing a free, online, adaptive
		training environment for College Board's Digital SAT. Deviating from their 98 years of tradition
		of paper-based examinations, College Board implemented sweeping changes to the SAT by shortening
		its length and transitioning to a fully digital format. As of July 2024, resources for the
		Digital SAT are far and few in between. We aim to bridge this gap by providing high-quality,
		accessible resources to equip students for test day.
	</p>
	<p class="text-xl text-center my-4">
		Sigma SAT is a community-driven initiative. Whether you are currently studying for the SAT or
		you have already achieved your dream score, there is a place for you at Sigma SAT. Anybody may
		contribute to our vast question database comprising of over 1500+ questions, and in return earn
		volunteer hours. Some questions are sourced from official College Board via their educator's
		questionbank. Others are either original questions by volunteers or inspired from previous
		Practice Exams and QAS from 2016-2023.
